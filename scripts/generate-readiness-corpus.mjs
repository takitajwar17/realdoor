import { readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const processed = join(root, "data", "processed");

const json = async (path) => JSON.parse(await readFile(join(processed, path), "utf8"));
const jsonl = async (path) =>
  (await readFile(join(processed, path), "utf8"))
    .trim()
    .split("\n")
    .filter(Boolean)
    .map((line) => JSON.parse(line));

function parseCsv(text) {
  const rows = [];
  let row = [];
  let value = "";
  let quoted = false;
  for (let index = 0; index < text.length; index += 1) {
    const character = text[index];
    if (character === '"') {
      if (quoted && text[index + 1] === '"') {
        value += '"';
        index += 1;
      } else quoted = !quoted;
    } else if (character === "," && !quoted) {
      row.push(value);
      value = "";
    } else if ((character === "\n" || character === "\r") && !quoted) {
      if (character === "\r" && text[index + 1] === "\n") index += 1;
      row.push(value);
      if (row.some(Boolean)) rows.push(row);
      row = [];
      value = "";
    } else value += character;
  }
  if (value || row.length) {
    row.push(value);
    rows.push(row);
  }
  const [headers, ...records] = rows;
  return records.map((record) => Object.fromEntries(headers.map((header, i) => [header, record[i]])));
}

const csv = async (path) => parseCsv(await readFile(join(processed, path), "utf8"));

const [rules, limits, checklists, qa, adversarial, documents, manifest, scenarios, fieldSchema] =
  await Promise.all([
    jsonl("rules.jsonl"),
    csv("mtsp-limits-2026.csv"),
    json("evaluation/application-checklists.json"),
    jsonl("evaluation/qa-gold.jsonl"),
    jsonl("evaluation/adversarial-tests.jsonl"),
    jsonl("gold/document-gold.jsonl"),
    csv("gold/document-manifest.csv"),
    csv("gold/scenario-gold.csv"),
    json("gold/field-schema.json"),
  ]);

const corpus = { rules, limits, checklists, qa, adversarial, documents, manifest, scenarios, fieldSchema };
const output = `// Generated by scripts/generate-readiness-corpus.mjs from data/processed. Do not edit.\nexport const GENERATED_READINESS_CORPUS = ${JSON.stringify(corpus, null, 2)} as const;\n`;
await writeFile(join(root, "src", "features", "readiness", "corpus.generated.ts"), output);

